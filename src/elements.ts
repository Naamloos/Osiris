import { $ } from './renderer';
import { VNode, VNodeChild, VNodeFactory } from './types';

function createVNodeFactory<T>(tagName: string): VNodeFactory<T> {
    return (...children: (VNodeChild | Partial<T>)[]) => {
        return $(tagName, ...children);
    }
}

